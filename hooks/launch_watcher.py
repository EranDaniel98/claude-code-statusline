"""SessionStart hook: launch watcher.py --tray if it isn't already running.

Invoked by Claude Code when a session starts (registered via
`watcher.py --register-claude-hook`). Drains stdin (Claude passes a JSON
payload we don't care about), checks for an existing tray watcher via
psutil, and if none is alive spawns one detached. Exits fast so Claude
Code's hook pipeline isn't blocked.

If multiple Claude sessions open in quick succession, the first hook
fires the spawn; the second's process scan picks up the running
watcher and is a no-op. Race window is small (the new watcher's
Popen starts within a few ms) — worst case briefly two watchers run
until pystray's NSStatusBar / NotifyIcon collisions sort themselves
out, never seen in practice.
"""
import os
import subprocess
import sys
from pathlib import Path


def _watcher_already_running() -> bool:
    """Return True iff some other process is running watcher.py --tray."""
    try:
        import psutil
    except ImportError:
        # No psutil → can't check; the caller will try to spawn and
        # rely on pystray's tray-icon collision to fail fast.
        return False
    me = os.getpid()
    for p in psutil.process_iter(["pid", "cmdline"]):
        try:
            if p.pid == me:
                continue
            cmdline = p.info.get("cmdline") or []
            has_watcher = any("watcher.py" in (a or "") for a in cmdline)
            has_tray = any("--tray" in (a or "") for a in cmdline)
            if has_watcher and has_tray:
                return True
        except (psutil.NoSuchProcess, psutil.AccessDenied):
            continue
    return False


def main() -> None:
    # Drain stdin so Claude Code's hook pipeline doesn't block on a
    # full pipe buffer.
    try:
        sys.stdin.read()
    except Exception:
        pass

    if _watcher_already_running():
        return

    here = Path(__file__).resolve().parent
    project_root = here.parent
    watcher_py = project_root / "watcher.py"
    if not watcher_py.exists():
        return

    if sys.platform == "win32":
        # Prefer the VBS launcher generated by --install-autostart: it
        # forces a truly hidden window even with uv's trampoline pythonw.
        vbs = project_root / "_claude_code_watcher_launch.vbs"
        if vbs.exists():
            try:
                subprocess.Popen(
                    ["wscript.exe", str(vbs)],
                    creationflags=0x08000000,  # CREATE_NO_WINDOW
                    close_fds=True,
                )
            except Exception:
                pass
            return
        # Fall back to pythonw.exe + CREATE_NO_WINDOW (less reliable
        # under uv but works in most setups).
        pyw = project_root / ".venv" / "Scripts" / "pythonw.exe"
        py = str(pyw) if pyw.exists() else sys.executable
        try:
            subprocess.Popen(
                [py, str(watcher_py), "--tray"],
                creationflags=0x08000000,
                close_fds=True,
                stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL,
            )
        except Exception:
            pass
    else:
        py_bin = project_root / ".venv" / "bin" / "python"
        py = str(py_bin) if py_bin.exists() else sys.executable
        try:
            subprocess.Popen(
                [py, str(watcher_py), "--tray"],
                start_new_session=True,
                close_fds=True,
                stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL,
            )
        except Exception:
            pass


if __name__ == "__main__":
    main()
