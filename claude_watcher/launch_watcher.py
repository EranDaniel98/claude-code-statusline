"""SessionStart hook: launch the tray watcher if it isn't already running.

Invoked by Claude Code when a session starts (registered via
`claude-watcher --register-claude-hook`). Drains stdin (Claude passes a
JSON payload we don't care about), checks for an existing tray watcher
via psutil, and if none is alive spawns one detached. Exits fast so
Claude Code's hook pipeline isn't blocked.

If multiple Claude sessions open in quick succession, the first hook
fires the spawn; the second's process scan picks up the running
watcher and is a no-op. Race window is small (the new watcher's Popen
starts within a few ms) — worst case briefly two watchers run until
pystray's NSStatusBar / NotifyIcon collisions sort themselves out,
never observed in practice.
"""
import os
import subprocess
import sys
import threading
from pathlib import Path


def _drain_stdin_with_timeout(seconds: float = 0.2) -> None:
    """Read stdin in a daemon thread; abandon after `seconds`.

    Claude Code's hook pipeline pipes a JSON payload that arrives
    instantly, so this returns quickly in the normal path. When the
    launcher is invoked via wscript (which doesn't attach stdin to
    anything useful), the read would otherwise hang forever — the
    timeout bounds that pathological case.
    """
    done = threading.Event()
    def _reader():
        try:
            sys.stdin.read()
        except Exception:
            pass
        finally:
            done.set()
    threading.Thread(target=_reader, daemon=True).start()
    done.wait(seconds)


def _watcher_already_running() -> bool:
    """Return True iff some other process is running the tray watcher."""
    try:
        import psutil
    except ImportError:
        # No psutil → can't check; the caller will try to spawn and
        # rely on pystray's tray-icon collision to fail fast.
        return False
    me = os.getpid()
    for p in psutil.process_iter(["pid", "cmdline"]):
        try:
            if p.pid == me:
                continue
            cmdline = p.info.get("cmdline") or []
            # Match both the legacy script-path form (watcher.py) and the
            # new module form (claude_watcher.watcher).
            joined = " ".join(a or "" for a in cmdline)
            if ("claude_watcher.watcher" in joined
                    or "claude_watcher/watcher.py" in joined
                    or "claude_watcher\\watcher.py" in joined
                    or "watcher.py" in joined) and "--tray" in joined:
                return True
        except (psutil.NoSuchProcess, psutil.AccessDenied):
            continue
    return False


def _watcher_launch_vbs() -> Path:
    """Path to the watcher VBS launcher generated by --install-autostart
    or --register-claude-hook on Windows."""
    return Path.home() / ".claude" / "_claude_watcher_launch.vbs"


def main() -> None:
    # Drain stdin so Claude Code's hook pipeline doesn't block on a full
    # pipe. Bounded with a timeout so a stdin-less invocation (wscript)
    # can't hang the launcher.
    _drain_stdin_with_timeout()

    if _watcher_already_running():
        return

    if sys.platform == "win32":
        vbs = _watcher_launch_vbs()
        if vbs.exists():
            try:
                subprocess.Popen(
                    ["wscript.exe", str(vbs)],
                    creationflags=0x08000000,  # CREATE_NO_WINDOW
                    close_fds=True,
                )
            except Exception:
                pass
            return
        # Fall back to pythonw + module form + CREATE_NO_WINDOW.
        pyw = Path(sys.executable).with_name("pythonw.exe")
        py = str(pyw) if pyw.exists() else sys.executable
        try:
            subprocess.Popen(
                [py, "-m", "claude_watcher.watcher", "--tray"],
                creationflags=0x08000000,
                close_fds=True,
                stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL,
            )
        except Exception:
            pass
    else:
        try:
            subprocess.Popen(
                [sys.executable, "-m", "claude_watcher.watcher", "--tray"],
                start_new_session=True,
                close_fds=True,
                stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL,
            )
        except Exception:
            pass


if __name__ == "__main__":
    main()
